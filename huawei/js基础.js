//如何定义变量
//变量:值会发生变化的量
//有两种方法：使用var关键字， 使用let关键字
//如下语句 分为三部分：var关键字，a变量名，变量值
var a=10;
//定义变量在控制台输出
console.log(a);
let b=20;
console.log(b);
